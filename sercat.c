int main(int argc, const char * argv[])
{

    if (argc < 2 || argc > 3)
    {
        fprintf(stderr, "usage: %s <device> [<file>]\n", argv[0]);
        return 1;
    }

    const char *device = argv[1];
    unsigned long bauds = 57600;
    int infd = STDIN_FILENO;
    if (argc == 3)
    {
        infd = open(argv[2], O_RDONLY | O_NONBLOCK);
        printf("Reading from %s\n", argv[2]);
        if (infd == -1) {
            perror("open");
            return 1;
        }
    }

    int fd = open(argv[1], O_RDWR | O_NONBLOCK | O_NOCTTY);
    if (fd == -1)
    {
        perror("can't open device");
        exit(errno);
    }

    struct termios config;
    if (tcgetattr(fd, &config) < 0)
    {
        perror("can't get serial attributes");
        exit(errno);
    }

    /* setup for non-canonical mode */
    cfmakeraw(&config);

    cfsetspeed(&config, bauds);

    config.c_cflag |= (CRTSCTS | CREAD);
    config.c_cflag &= ~CSTOPB;
    config.c_cflag &= ~CLOCAL;

    config.c_cc[VMIN]  = 1;
    config.c_cc[VTIME] = 1;

    if (tcsetattr(fd, TCSAFLUSH, &config) < 0)
    {
        perror("can't set serial attributes");
        exit(errno);
    }

    fflush(stdout);

    tcgetattr(STDIN_FILENO, &config);
    stdout_settings = config;
    signal(SIGINT, sigint);
    signal(SIGQUIT, sigint);
    config.c_lflag &=(~ICANON & ~ECHO);
    tcsetattr(STDIN_FILENO, TCSANOW, &config);

    int kq = kqueue();
    struct kevent evset[3];
    EV_SET(&evset[0], fd, EVFILT_READ, EV_ADD, 0, 0, NULL);
    EV_SET(&evset[1], fd, EVFILT_WRITE, EV_ADD, 0, 0, NULL);
    EV_SET(&evset[2], infd, EVFILT_READ, EV_ADD, 0, 0, NULL);
    kevent(kq, evset, 3, NULL, 0, NULL);

    struct kevent evList[32];
    char buffer[2048];
    size_t ready = 0;
    while (!quitit) {
        int nev = kevent(kq, NULL, 0, evList, 32, NULL);
        for (int i = 0; i < nev; i++) {
            if (evList[i].ident == infd && ready < 2048) {
                size_t new = read(infd, buffer+ready, 2048 - ready);
                if (new > 0) ready += new;      // how many more bytes are ready to send?
            } else if (evList[i].ident == fd) {
                if (evList[i].flags & EV_EOF) {
                    printf("EOF on TTY device\n");
                    quitit = 1;
                } else if (evList[i].filter == EVFILT_READ) {
                    char input;
                    if (read(fd, &input, 1) == 1) {
                        if (isprint(input) || input == 13 || input == 10) {
                            write(STDOUT_FILENO, &input, 1);
                        } else {
                            const char *hex = "0123456789abcdef";
                            write(STDOUT_FILENO, &hex[(input & 0xf0) >> 4], 1);
                            write(STDOUT_FILENO, &hex[(input & 0x0f)], 1);
                        }
                        if (input == 'C' && ready == 0) {
                            #undef BOOTSTRAP
                            #ifdef BOOTSTRAP
                            memcpy(buffer, bootstrap, sizeof(bootstrap));
                            ready = sizeof(bootstrap);
                            #else
                            memcpy(buffer, ym_zerofile, sizeof(ym_zerofile));
                            ready = sizeof(ym_zerofile);
                            #endif
                            printf("\nTransmitting zerofile, %lu bytes\n", ready);
                            fflush(stdout);
                        }
                    }
                } else if (evList[i].filter == EVFILT_WRITE && ready > 0) {
                    ssize_t sent = write(fd, buffer, 1);
                    if (sent == 1) {
                        char echo = isprint(*buffer) ? *buffer : '.';
                        write(STDOUT_FILENO, &echo, 1);
                        memcpy(buffer, buffer+1, 1023);
                        ready -= 1;
                        usleep(50000);
                    }
                }
            }
        }
    }

    tcsetattr(STDIN_FILENO, TCSANOW, &stdout_settings);

    close(fd);
    return 0;
}
